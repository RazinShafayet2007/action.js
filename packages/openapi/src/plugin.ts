import { action, type ActionDefinition, type ActionResponseDefinitions, type HttpMethod } from "@action-js/core";
import { z } from "zod";

import { createOpenApiDocument } from "./document.js";
import type { OpenApiDocument, OpenApiInfo, OpenApiServer } from "./types.js";

type AnyAction = ActionDefinition<HttpMethod, string, any, any, any, any, any, any, any>;

type PluginAction<TServices, TContext extends object> = ActionDefinition<
  HttpMethod,
  string,
  TServices,
  TContext,
  any,
  any,
  any,
  ActionResponseDefinitions | undefined,
  any
>;

export interface OpenApiPlugin<TServices, TContext extends object = {}> {
  name: string;
  actions?: ReadonlyArray<PluginAction<TServices, TContext>> | undefined;
}

export interface OpenApiPluginOptions {
  info: OpenApiInfo;
  actions: ReadonlyArray<AnyAction> | (() => ReadonlyArray<AnyAction>);
  servers?: OpenApiServer[] | undefined;
  jsonPath?: string | undefined;
  docsPath?: string | undefined;
}

export function openApiPlugin<TServices, TContext extends object = {}>(
  options: OpenApiPluginOptions,
): OpenApiPlugin<TServices, TContext> {
  const jsonPath = options.jsonPath ?? "/openapi.json";
  const docsPath = options.docsPath ?? "/docs";

  return {
    name: "openapi",
    actions: [
      action({
        method: "GET",
        path: jsonPath,
        response: {
          200: z.any(),
        },
        handler: () => ({
          status: 200 as const,
          body: createDocument(options),
        }),
      }),
      action({
        method: "GET",
        path: docsPath,
        handler: () =>
          new Response(renderDocsHtml(options.info.title, jsonPath), {
            status: 200,
            headers: {
              "content-type": "text/html; charset=utf-8",
            },
        }),
      }),
    ],
  };
}

function createDocument(options: OpenApiPluginOptions): OpenApiDocument {
  return createOpenApiDocument({
    info: options.info,
    actions: resolveActions(options.actions),
    servers: options.servers,
  });
}

function resolveActions(actions: OpenApiPluginOptions["actions"]): ReadonlyArray<AnyAction> {
  return typeof actions === "function" ? actions() : actions;
}

